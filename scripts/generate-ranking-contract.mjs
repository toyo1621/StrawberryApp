import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';

const root = resolve(import.meta.dirname, '..');
const contractPath = resolve(root, 'contracts/rankings.json');
const contract = JSON.parse(readFileSync(contractPath, 'utf8'));

const requireValue = (condition, message) => {
  if (!condition) {
    throw new Error(`Invalid rankings contract: ${message}`);
  }
};

requireValue(Number.isInteger(contract.apiVersion) && contract.apiVersion > 0, 'apiVersion must be positive.');
requireValue(
  Number.isInteger(contract.gameSessionTtlMs) && contract.gameSessionTtlMs >= 60_000,
  'gameSessionTtlMs must be at least one minute.',
);
requireValue(
  Number.isInteger(contract.pendingSyncIntervalMs)
    && contract.pendingSyncIntervalMs >= 5_000
    && contract.pendingSyncIntervalMs < contract.gameSessionTtlMs,
  'pendingSyncIntervalMs must be at least five seconds and shorter than the game session TTL.',
);
requireValue(Array.isArray(contract.gameTypes) && contract.gameTypes.length > 0, 'gameTypes are required.');
requireValue(Array.isArray(contract.rankingPeriods) && contract.rankingPeriods.length > 0, 'rankingPeriods are required.');
requireValue(Array.isArray(contract.islandRegions) && contract.islandRegions.length > 0, 'islandRegions are required.');

const assertUnique = (values, label) => {
  requireValue(new Set(values).size === values.length, `${label} must be unique.`);
};
assertUnique(contract.gameTypes, 'gameTypes');
assertUnique(contract.rankingPeriods, 'rankingPeriods');
assertUnique(contract.islandRegions.map(({ key }) => key), 'island region keys');
assertUnique(contract.islandRegions.map(({ value }) => value), 'island region values');
contract.islandRegions.forEach(({ key, value }) => {
  requireValue(/^[A-Z][A-Z0-9_]*$/.test(key), `invalid island region key: ${key}`);
  requireValue(/^[a-z][a-z0-9_]*$/.test(value), `invalid island region value: ${value}`);
});

const quoted = (values) => values.map((value) => `  '${value}',`).join('\n');
const regionObject = contract.islandRegions
  .map(({ key, value }) => `  ${key}: '${value}',`)
  .join('\n');
const sourceBanner = '// Generated by scripts/generate-ranking-contract.mjs. Do not edit directly.\n';
const typescriptSource = `${sourceBanner}
export const RANKINGS_API_VERSION = ${contract.apiVersion} as const;
export const GAME_SESSION_TTL_MS = ${contract.gameSessionTtlMs} as const;
export const PENDING_SYNC_INTERVAL_MS = ${contract.pendingSyncIntervalMs} as const;

export const API_GAME_TYPES = [
${quoted(contract.gameTypes)}
] as const;

export const RANKING_PERIODS = [
${quoted(contract.rankingPeriods)}
] as const;

export const ISLAND_REGION = {
${regionObject}
} as const;

export const ISLAND_REGIONS = [
${quoted(contract.islandRegions.map(({ value }) => value))}
] as const;
`;
const javascriptSource = `${sourceBanner}
export const RANKINGS_API_VERSION = ${contract.apiVersion};
export const GAME_SESSION_TTL_MS = ${contract.gameSessionTtlMs};
export const PENDING_SYNC_INTERVAL_MS = ${contract.pendingSyncIntervalMs};
export const API_GAME_TYPES = ${JSON.stringify(contract.gameTypes)};
export const RANKING_PERIODS = ${JSON.stringify(contract.rankingPeriods)};
export const ISLAND_REGION = ${JSON.stringify(Object.fromEntries(
  contract.islandRegions.map(({ key, value }) => [key, value]),
))};
export const ISLAND_REGIONS = ${JSON.stringify(contract.islandRegions.map(({ value }) => value))};
`;

const outputs = new Map([
  [resolve(root, 'src/generated/rankingContract.ts'), typescriptSource],
  [resolve(root, 'worker/src/generated/rankingContract.ts'), typescriptSource],
  [resolve(root, 'scripts/generated/rankingContract.mjs'), javascriptSource],
]);
const checkOnly = process.argv.includes('--check');
const stale = [];

for (const [path, expected] of outputs) {
  let current = null;
  try {
    current = readFileSync(path, 'utf8');
  } catch {
    current = null;
  }
  if (current === expected) {
    continue;
  }
  if (checkOnly) {
    stale.push(path);
    continue;
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, expected);
}

if (stale.length > 0) {
  throw new Error(`Generated ranking contracts are stale:\n${stale.join('\n')}`);
}
console.log(checkOnly ? 'Generated ranking contracts are current.' : 'Generated ranking contracts updated.');
